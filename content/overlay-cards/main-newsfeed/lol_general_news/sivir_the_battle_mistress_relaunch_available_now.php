          <div class="field field-name-body field-type-text-with-summary field-label-hidden"><p>Sivir, the Battle Mistress has been relaunched! Featuring improved fidelity and readability in game, Sivir returns to the battlefields wielding her newly re-emphasized boomerang blade to accompany creative updates as well as a kit rework. </p>

<p>As one of the earliest champions in the game, Sivir's visuals and effects didn't match the look and feel of newer champs. We've designed her silhouette around her weapon while improving fidelity and readability of Sivir in-game, giving her an aesthetic that matches the battle-hardened mercenary personality she's always had. New effects on her abilities emphasize the boomerang-like nature of her weapon, and should make playing Sivir feel like dashing around the fight tossing ricocheting blades and whittling enemies down from unexpected angles. </p>

<p>Along with her visuals, we identified a few mysteries we could explore in Sivir's lore. Our goals were to clearly identify her place in the world and deliver on the mercenary fantasy inherent in her story. Generic battle platitudes and lack of deep motivation previously made it difficult to become personally invested in her successes or failures. We've added new voice-over that ties in to her expanded lore and relationships. With more of her back story and a glimpse into her possible future revealed, players will have more opportunities to learn about Sivir. </p>

<header class="header-primary"><h3>Gameplay</h3></header><p>From a gameplay perspective, Sivir has a unique feel among ranged marksmen. She's a powerful counter to spell-reliant champs and offers different skills for teamfights than many marksmen with her ultimate and passive. It was extremely important to us to ensure that any changes to her kit preserved her ability to split-push, initiate and counter-initiate while opening up new options for Sivir players. With that in mind, Boomerang Blade and Spell Shield still operate the same way. Ricochet can bounce to more enemies, although it cannot hit the same target twice. We've also reduced the cooldown of On the Hunt, and redesigned it to deliver a massive move speed boost and no longer have a cast time. </p>


