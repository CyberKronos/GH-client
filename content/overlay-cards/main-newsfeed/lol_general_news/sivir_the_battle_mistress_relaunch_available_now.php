<header class="header-primary"><h3>Story</h3></header><p>When the people of Valoran speak of the ''Battle Mistress,'' there is only one woman to whom they refer. Combining unflinching bravery with prowess in combat and endless ambition, Sivir's reputation as a fortune hunter is only rivaled by her amassed wealth and acclaim. Faced with the revelation of her mysterious heritage, Sivir must now weigh her desire to follow her own path against the burden of a greater legacy. </p>
<p>Sivir grew up among the desert nomads of Shurima, mastering the art of tomb robbing and enjoying a life without strong ties to any one place. She excelled at the mercenary life, accumulating riches, followers and reputation with ease. As her fame spread beyond the desert sands, she caught the attention of wealthy patrons across all of Valoran. The most influential of these benefactors was Noxus High Command. Sivir served the whims of the brutal nation with little concern for consequence. Their partnership proved to be a profitable one until the Ionian conflict broke out. She chose to take no part in the messy invasion and severed her ties. </p>
<p>It was no surprise to Sivir when the Noxians next paid her a visit, but where she expected assassins, she instead received an offer. Cassiopeia, the daughter of one of Noxus's most influential families, claimed to have unearthed the location of ancient Shurima's greatest treasure. All that the noblewoman required was an experienced tomb robber as an escort. </p>
<p>The expedition proved to be more than Sivir and Cassiopeia bargained for. The ancient tomb held neither the treasure Cassiopeia had promised, nor the weapon she coveted. Despite Sivir's warnings, the Noxian flung open the door to the entombed vault, setting events in motion that would change not just Sivir's life, but all of Shurima forever. </p>
