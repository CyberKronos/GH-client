<hr class="divider"><header class="header-subtle"><h4>Early-game</h4></header><p> As the laning phase begins, Sivir has a number of tools available to determine how she wants to approach the early-game. Sustain-heavy options involve using Spell Shield to regenerate mana and Boomerang Blade to harass lane opponents rather than push. Aggressive lanes might opt for heavy use of Ricochet to clear minion waves rapidly, forcing opponents up against their own turret. </p>

<hr class="divider"><header class="header-subtle"><h4>Midgame</h4></header><p> Sivir transitions well into the objective-heavy portions of the game. Because of her enormous contribution to team mobility, it becomes easy to rotate around the jungle, contesting neutral buffs and larger objectives like dragons or turrets.  The reasonably low cooldown of On the Hunt makes it difficult for the enemy team to keep up with a Sivir-led composition. </p>
<hr class="divider"><header class="header-subtle"><h4>Late-game</h4></header><p>Late-game Sivir acts as a potent split-pusher if the team composition calls for it. Thanks to her built-in movement speed steroid that can now be cast on the run, Sivir can unleash tons of damage on turrets before escaping when she needs to, blocking incoming abilities with Spell Shield along the way. For more engage-heavy teams, Sivir offers massive area of effect damage through Ricochet and maneuverability with On the Hunt. A poke-comp can initiate quick engages with Sivir-aided disengages. Using this strategy takes advantage of the amount of damage Sivir can dish out, while helping to protect more fragile poke-oriented teams. </p>
