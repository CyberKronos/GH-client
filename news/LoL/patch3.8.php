				<h5>Changes to Slow Zones</h5>

				<p>All slow zones now update on a quarter-second interval, meaning when you move out of a slow field, the slow will wear off almost immediately unless you’re hit by an ability that applies an individual slow (like Kog’Maw’s Void Ooze). What this means: your champion's movement speed will be restored more quickly when leaving a slow field, but the slow within the fields have not been changed.</p>

				<p>This change should make interacting with slow zones more intuitive while rewarding players who react quickly (as stepping out of a slow field will reward you faster). This is a small nerf to most of the champions affected, but we will be closely monitoring champions affected by these changes.</p>
		
				<p>The following abilities have had their slow refresh rate reduced to 0.25 seconds:</p>
