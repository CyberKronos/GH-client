          <div class="field field-name-body field-type-text-with-summary field-label-hidden"><p>Break out your best set of Mundo-style cleavers and carve your way to every Harrowing skin ever created. This year, we're celebrating Halloween with a League of Legends twist: pumpkins with images of your favorite League content. What happens when you combine the Halloween spirit with League of Legends? We're looking for the most inspiring and spookiest — or maybe most adorable — Teem-O'-Lanterns. Share 'em with us and you could win all — yes, ALL — the Harrowing skins we've ever released.</p>

<hr class="divider"><p><strong><h4>Contest Entry</h4></strong></p>

<p>How can you take part in our Halloween competition?</p><ul><li>Carve up a pumpkin with a League of Legends-related image or theme</li><li>Share it with us via Twitter using the #LOLHalloweenContest hash tag or by <strong><a href="http://woobox.com/sxnudj">submitting it here</a></strong></li></ul><p>Don't forget to carve your summoner name and light a candle inside the pumpkin so we can see it glow. We'll select winners based on the quality of the submission, originality and whether the entry follows the contest rules!</p>

<hr class="divider"><p><strong><h4>Prizes</h4></strong></p>

<p>Saying every Harrowing skin isn't enough. Here's the list of what you'll earn if you win:</p><ul><li>Mundo Mundo</li><li>Pumpkinhead Fiddlesticks</li><li>Kitty Cat Katarina</li><li>Zombie Ryze</li><li>Lollipoppy</li><li>Nosferatu Vladimir</li><li>Franken Tibbers Annie</li><li>Definitely Not Blitzcrank</li><li>Bewitching Nidalee</li><li>Haunting Nocturne</li><li>Headless Hecarim</li><li>Haunted Maokai</li><li>Underworld Twisted Fate</li><li>Headmistress Fiora</li><li>Pirate Ryze</li><li>Zombie Brand</li><li>Haunted Zyra</li><li>Officer Vi</li></ul><p>If the champions are not already owned, the winners will receive them in addition to the skins.</p>

<hr class="divider"><p>So grab your tools, get creative and share your work as the contest ends on <strong>November 3</strong>. We'll be announcing the first round of six winners on <strong>October 28</strong> and the second round of the remaining six on <strong>November 4</strong>.</p>

<p>For all of the lovable legalese around our NA contests, <strong><a href="http://na.leagueoflegends.com/en/page/forum-contest-master-rules">click here</a></strong>. Good luck everyone, and happy Halloween!</p>

