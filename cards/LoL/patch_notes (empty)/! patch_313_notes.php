	<header class="header-primary pn_heading"><a class="anchor" name="patch-Champions" id="patch-Champions"></a>
		<h2>
			Champions
		</h2>
	</header><p>
		<a class="anchor" name="patch-aatrox" id="patch-aatrox"></a><strong class="aatrox">Aatrox</strong>
	</p>
	<p class="pn_darkred">
		<strong class="pn_black">Summary:</strong> Blood Well’s passive attack speed bonus now scales with character level (lower at earlier levels, higher at max level). Blades of Torment’s damage has been reduced (equal at lower levels, lower at max levels).
	</p>
	<p class="pn_darkred">
		<strong class="pn_black">Context:</strong> Any champion can be frustrating to play against when they’re capable of building very tanky while still outputting considerable DPS, so we're lowering some of Aatrox’s innate damage. With this change, Aatrox must build items for damage and won’t be able to simultaneously tank and smash face as easily.
	</p>

	<p>
		<strong>Blood Well</strong>
	</p>
	<ul><li>Passive Attack Speed changed to 30/35/40/45/50/55% (upgrades every 3 champion levels) (from 50% flat)
		</li>
	</ul><p>
		<strong>Blades of Torment</strong>
	</p>
	<ul><li>Damage reduced to 75/110/145/180/215 (from 75/120/165/210/255)
		</li>
	</ul><hr class="divider"><p>
		<a class="anchor" name="patch-ahri" id="patch-ahri"></a><strong class="ahri">Ahri</strong>
	</p>
	<p class="pn_darkred">
		<strong class="pn_black">Summary:</strong> Charm has had its mana cost changed to a flat amount at all levels but now increases the magic damage Ahri deals to a Charmed target. Essence Theft now heals her for a specific amount based on the number of enemies she hits with her passive-enhanced spell, as opposed to getting straight spell vamp. Fox Fire now deals less damage when multiple wisps hit the same target. Spirit Rush's base damage and AP ratios have been slightly reduced.
	</p>
	<p class="pn_darkred">
		<strong class="pn_black">Context:</strong> Ahri is a champion with a lot of strengths and few weaknesses, given her high versatility and reliability, so she can consistently apply a lot of pressure on her opponents from relative safety. Ahri’s strengths should be better defined than they currently are. These changes give Ahri players (and her opponents) clear areas of mastery that differentiate the good Ahris from the great Ahris. Our core focuses are:
	</p>

	<ul><li>Make Ahri’s default lane patterns more clear, so both Ahri and her opponents can make plays around them
		</li>
		<li>Make Ahri’s “all-in” gameplay more closely tied with landing Charm to establish Charm as Ahri’s “kiss of death” that needs to be avoided, as well as lower her threat/presence while Charm is on cooldown
		</li>
		<li>Keep Ahri’s core gameplay intact while trimming away strength from the low-gameplay patterns that don’t feel rewarding
		</li>
	</ul><p>
		<strong>Essence Theft</strong>
	</p>
	<ul><li>Now heals Ahri for 2 (+1 per champion level) (+0.09 Ability Power) each time her passive-enhanced spells hit an enemy (previously 35% Spell Vamp)
		</li>
	</ul><p>
		<strong>Fox-Fire</strong>
	</p>
	<ul><li>Mana cost reduced to 50 (from 60)
		</li>
		<li>Diminishing returns effect on same-target hits increased to 70% (from 50%) (best case is now 100% + 30% + 30%)
		</li>
		<li>This reduces the same-target damage to 64/104/144/184/224 (+0.64 Ability Power) (from 80/130/180/230/280 (+0.8 Ability Power))
		</li>
		<li>Fixed a bug that caused Fox-Fire to sometimes deal reduced damage to a target that should receive the standard damage amount
		</li>
	</ul><p>
		<strong>Charm</strong>
	</p>
	<ul><li>Now increases the magic damage Ahri deals to the target by 20% for 6 seconds
		</li>
		<li>Mana cost changed to 85 (from 50/65/80/95/110)
		</li>
	</ul><p>
		<strong>Spirit Rush</strong>
	</p>
	<ul><li>Base damage reduced to 70/110/150 (+0.3 Ability Power) (from 85/125/165 (+0.35 Ability Power))
		</li>
		<li>This reduces the max damage per target to 210/330/450 (+0.9 Ability Power) (from 255/375/495 (+1.05 Ability Power))
		</li>
	</ul><hr class="divider"><p>
		<a class="anchor" name="patch-corki" id="patch-corki"></a><strong class="corki">Corki</strong>
	</p>
	<p class="pn_darkred">
		<strong class="pn_black">Summary:</strong> Corki's base attack speed has been reduced. Missile Barrage's base damage has been reduced at earlier ranks and the cooldown between missile shots has been increased. Additionally, the cooldown between missile shots can no longer be reduced by cooldown reduction, but CDR will still affect how quickly you can gain missile ammo.
	</p>
	<p class="pn_darkred">
		<strong class="pn_black">Context:</strong> Overall, we like the direction we pushed Corki toward with Trinity Force, but currently he's too overwhelming to play against. Here we're reducing excess power in a few areas to put him more in line with other similar spell-based ADCs. Ultimately, Missile Barrage should function as a powerful sustained damage tool rather than a source of burst damage.
	</p>

	<p>
		<strong>General</strong>
	</p>
	<ul><li>Base Attack Speed reduced to 0.625 (from 0.658)
		</li>
	</ul><p>
		<strong>Missile Barrage</strong>
	</p>
	<ul><li>Base damage reduced to 100/180/260 (from 120/190/260)
		</li>
		<li>Cooldown between missile shots increased to 2 seconds (from 1.2)
		</li>
		<li>Cooldown between missile shots can no longer be reduced by Cooldown Reduction (Cooldown Reduction still affects how quickly you gain missile ammo)
		</li>
	</ul><hr class="divider"><p>
		<a class="anchor" name="patch-fizz" id="patch-fizz"></a><strong class="fizz">Fizz</strong>
	</p>
	<p class="pn_darkred">
		<strong class="pn_black">Summary:</strong> The active ability power ratio of Seastone Trident has been reduced significantly.
	</p>
	<p class="pn_darkred">
		<strong class="pn_black">Context:</strong> We’re aiming to reduce some of Fizz’s late game damage so that he has to rely more on successfully landing his ultimate or finding a way to stall until his next set of cooldowns come up in order to successfully assassinate targets in late game fights.
	</p>

	<p>
		<strong>Playful / Trickster</strong>
	</p>
	<ul><li>Fixed a bug where Fizz became targetable while still descending from Playful (he now only becomes targetable after landing, just like Trickster)
		</li>
	</ul><p>
		<strong>Seastone Trident</strong>
	</p>
	<ul><li>Active Ability Power ratio reduced to 0.15 (from 0.35)
		</li>
	</ul><hr class="divider"><p>
		<a class="anchor" name="patch-heimerdinger" id="patch-heimerdinger"></a><strong class="heimerdinger">Heimerdinger</strong>
	</p>
	<p class="pn_darkred">
		<strong class="pn_black">Summary:</strong> Heimerdinger’s kit has been significantly reworked this patch. Techmaturgical Repair Bots no longer works on friendly turrets. H-28G Evolution Turrets now fire a beam every twelve seconds in a projected line shot in addition to their regular attacks. Additionally, H-28G Evolution Turret will now shut down after a period of time if Heimerdinger leaves the area. Hextech Micro-Rockets have been reworked to unleash a barrage of rockets that converge at your cursor before fanning out. Enemies hit by more than one rocket take reduced magic damage from additional rockets. CH-1 Electron Storm Grenade now travels in an arc.<br><br>
		UPGRADE!!! makes Heimerdinger’s next spell free while also applying bonus effects. Upgrading H-28G Evolution Turret transforms it into a giant H-28Q Apex Turret that deals higher damage with a lower cooldown beam. Upgrading Hextech Micro-Rockets transforms it into Hextech Rocket Swarm, where Heimerdinger fires out four waves of rockets that deal damage on impact. Finally, upgrading the CH-1 Electron Storm Grenade transforms it into a CH-3X Lightning Grenade that bounces three times (dealing damage and stunning with each bounce) before disappearing.
	</p>
	<p class="pn_darkred">
		<strong class="pn_black">Context:</strong> Heimerdinger’s got some new toys! Our core goal was to give Heimerdinger the tools to differentiate himself in ability use and build optimization. In particular, we focused on diversifying the Heimerdinger experience so that each of Heimer’s abilities comes with its own form of gameplay and interaction – players will need to think carefully about how to use each in an optimal way. Raise your turrets!
	</p>

	<p>
		<strong>Passive: Techmaturgical Repair Bots</strong>
	</p>
	<ul><li>Heimerdinger gives nearby allied champions and H-28G Evolution Turrets 10/15/20/25/30 bonus Health Regen
		</li>
	</ul><p>
		<strong>H-28G Evolution Turret</strong>
	</p>
	<ul><li>Places a Turret at target location. Turret attacks prioritize Heimerdinger’s targets and enemies attacking Heimerdinger
		</li>
		<li>Heimerdinger generates a Turret Kit every 24/23/22/21/20 seconds (modified by Cooldown Reduction), and he can hold up to 1/1/2/2/3 Kits at once
		</li>
		<li>Cost: 20 Mana and 1 Kit
		</li>
		<li>Cooldown: 1s
		</li>
		<li>
			<strong>H-28G Evolution Turret Stats</strong>
			<ul><li>Health: 150 (+25 per champion level)
				</li>
				<li>Attack - Cannon: 15/22/29/36/42 (+0.125 Ability Power) magic damage
				</li>
				<li>Attack - Beam: 50/75/100/125/150 (+0.5 Ability Power) magic damage in a line, 12 second cooldown
				</li>
				<li>Maximum Turrets: 3
				</li>
			</ul></li>
	</ul><p>
		<strong>Hextech Micro-Rockets</strong>
	</p>
	<ul><li>Unleashes a barrage of 5 rockets that converge towards your cursor and fan out past it. Each rocket deals 60/90/120/150/180 (+0.45 Ability Power) magic damage
		</li>
		<li>Enemies that are hit by more than one rocket take 20% magic damage for each additional rocket (max damage 108/162/216/270/324). Minions take 60% instead
		</li>
		<li>Targeting the ability closer to Heimerdinger increases projectile spread
		</li>
		<li>Cost: 70/80/90/100/110 Mana
		</li>
		<li>Cooldown: 11s
		</li>
	</ul><p>
		<strong>CH-1 Electron Storm Grenade</strong>
	</p>
	<ul><li>Hurls a grenade that deals 60/100/140/180/220 (+0.6 Ability Power) magic damage to enemy units and slows their Movement Speed by 35% for 2 seconds
		</li>
		<li>Enemies in the center of the blast are also stunned for 1.25 seconds
		</li>
		<li>Cost: 85 Mana
		</li>
		<li>Cooldown: 18/16/14/12/10
		</li>
	</ul><p>
		 <strong>UPGRADE!!!</strong>
	</p>
	<ul><li>Heimerdinger's next basic spell is free and has bonus effects
		</li>
		<li>
			<strong>H-28Q Apex Turret:</strong> Places a Turret that deals 90/110/130 (+0.33 Ability Power) magic damage with its cannon and 225/300/375 (+0.8 Ability Power) magic damage with its beam for 8 seconds
		</li>
		<li>
			<strong>Hextech Rocket Swarm:</strong> Fires 4 waves that deal 135/180/225 (+0.45 Ability Power) magic damage each. Champions and monsters hit by multiple rockets take reduced damage (max 550/690/865 (+1.83 Ability Power))
		</li>
		<li>
			<strong>CH-3X Lightning Grenade:</strong> Throws a grenade that discharges three times, dealing 150/200/250 (+0.6 Ability Power) magic damage each time. Both the stun and slow areas are larger, and the slow is improved to 80%
