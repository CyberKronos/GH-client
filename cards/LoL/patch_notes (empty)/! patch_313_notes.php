	<header class="header-primary pn_heading"><a class="anchor" name="patch-In-Game-Mechanics" id="patch-In-Game-Mechanics"></a>
		<h2>
			In-Game Mechanics
		</h2>
	</header><p>
		<strong>Attack Speed slows</strong>
	</p>
	<p class="pn_darkred">
		<strong class="pn_black">Summary:</strong> Attack speed slows (IE: Nasus' Wither) were immune to cleanse or other forms of debuff removal unless attached to a movement impairing effect. Now all attack speed slows can be cleansed and are treated like movement speed slows.
	</p>
	<p class="pn_darkred">
		<strong class="pn_black">Context:</strong> We’re updating attack speed slows in order to have more consistent counterplay related to debuff removal. Essentially, we're looking to treat all slows the same, whether they're movement or attack speed based.
	</p>

	<ul><li>Attack Speed slows can now be cleansed as if they were movement-impairing crowd control effects
		</li>
		<li>
			<strong>Affected champion abilities and items:</strong>
			<ul><li>Gragas’ Barrel Roll
				</li>
				<li>Malphite’s Ground Slam
				</li>
				<li>Randuin's Omen
				</li>
				<li>Warden's Mail
				</li>
			</ul></li>
	</ul><hr class="divider"><p>
		<strong>Champions with dashes tied to their ultimates</strong>
	</p>
	<p class="pn_darkred">
		<strong class="pn_black">Summary:</strong> The following champions will now be affected by snaring or stunning crowd control abilities after they complete their dashes. Their spells will still be unstoppable, but most forms of crowd control landed while the champion’s using the ability will be applied after the spell completes. CC duration will tick down while a champion is mid-dash.
	</p>
	<p class="pn_darkred">
		<strong class="pn_black">Context:</strong> We're adding more counterplay for champions who have long-range dashes tied to their ultimates by making them susceptible to certain forms of crowd control after they complete a dash - like snares or stuns. Now if a teammate lands a snare in the middle of a champion's dash, the ulting champion will complete their ability, but be snared at the end.
	</p>

	<ul><li>Crowd control effects can now be received during "unstoppable" movement ultimates (they previously discarded all crowd control effects), but those ultimates will still finish their movement regardless (crowd control effects can persist after the cast finishes)
		</li>
		<li>
			<strong>Affected champions:</strong>
			<ul><li>Jarvan IV's Cataclysm
				</li>
				<li>Nocturne's Paranoia
				</li>
				<li>Vi's Assault and Battery
				</li>
				<li>Malphite's Unstoppable Force
				</li>
				<li>Hecarim's Onslaught of Shadows
				</li>
			</ul></li>
	</ul><hr class="divider"><p>
