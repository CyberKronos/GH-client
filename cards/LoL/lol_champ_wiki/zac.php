<p>Each time Zac uses an ability on enemies, he sheds a chunk of himself to a nearby location. Zac can move over these chunks to reabsorb them, regaining <span>4% of his maximum health</span>. Enemies may move over these chunks to destroy them.
</p><p>Upon taking fatal damage, Zac splits into 4 bloblets that attempt to recombine. Each bloblet has <span>12% of Zac's maximum health</span>, and 50% of his armor and magic resistance. If any of these bloblets remain after 8 seconds, Zac will revive with 10-50% health depending on the health of the surviving chunks. This effect has a static cooldown and is unaffected by cooldown reduction.
</p>
