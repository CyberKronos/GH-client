          <p>
            <div>
<p><span>Active:</span> Thresh throws out his scythe to latch onto the first enemy hit for 1.<small>5</small> seconds, dealing magic damage and stunning them for the same duration. During this time Thresh cannot attack but will tug on his chains twice over the duration, each time pulling the hooked unit a short distance towards himself.
</p><p>While a target is hooked, Thresh can reactivate this ability to pull himself to the bound enemy. This removes the stun but allows Thresh to attack again.
