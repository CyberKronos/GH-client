          <h6>Dark Passage</h6>
          <div class="champion-wiki-side-by-side-image">
            <img class="rounded_pics" src="http://images3.wikia.nocookie.net/__cb20130110022508/leagueoflegends/images/7/78/DarkPassage.jpg">
          </div>
          <div class="champion-wiki-side-by-side-text">
            <ul><li><span><span>Range:</span></span> 950</li><li><span><span>Cooldown:</span></span> 22 / 20.<small>5</small> / 19 / 17.<small>5</small> / 16</li><li><span><span>Cost:</span></span> 50 / 55 / 60 / 65 / 70 <span>mana</span></li></ul>
          </div>
          <p>
            <div>
<p><span>Active:</span> Thresh throws his lantern to the target location, which remains there for up to 6 seconds. If an ally right-clicks it, they pick up the lantern and Thresh pulls them both back to his own location. If Thresh moves more than ~1500 units away, the lantern will return to him. 
</p><p>For the next 6 seconds, allies who come near the lantern (even while Thresh is holding it) gain a shield that absorbs damage for up to 4 seconds. Allies can only receive the shield once per cast.
</p>
</div>
          </p>

