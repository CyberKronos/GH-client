<header class="header-primary pn_heading"><a class="anchor" name="01-16-hotfix" id="01-16-hotfix"></a>
		<h2>
			01/16/2014 Leagues Patch
		</h2>
	</header><p class="pn_darkred">The 2014 season began and Leagues have been reset. A lot of our changes are focused on observations from last season’s ranked play. When we first made leagues we wanted to combat “ladder anxiety” where players were too nervous about losing their rank to actually play. However, what we ended up seeing were group of players who would reach division V of a tier and then tank their games as they exploited demotion immunity.</p>
<p class="pn_darkred">No bueno.</p>

<p class="pn_darkred">For the 2014 season, it’s possible for a player to get demoted out of a league if they continuously play at a much lower skill level while losing games. You’ll be sufficiently warned of the demotion if it looms, but the change will improve ranked play at division V tiers.</p>

<p class="pn_darkred">We also removed “LP clamping” where players would only see tiny LP gains at division 1 of their tier. Clamping will still be in effect at Diamond I (because Challenger should rep the best players, so it needs to be strict), but all other tiers will see regular LP gains from now on.</p>

<p class="pn_darkred">Alright, I’m gonna go start my placement matches.</p>

<p class="pn_darkred">-Pwyff</p>

<header class="header pn_heading"><h2>Leagues</h2>
	</header><p></p>
<ul><li>Ranked solo, duo, and ranked team stats have been reset for the start of the 2014 season
<ul><li>Play 10 games in ranked solo or duo queue, or 5 games in ranked teams to be seeded into a league!</li>
<li>Ranked inactivity will be reset and re-enabled for the start of the season</li>
</ul></li>
<li>Changed LP calculations so players will no longer see a sudden decrease in gains when hitting division I in all tiers (except Diamond)</li>
<li>Tier demotions and shielding have been added
<ul><li>Once you enter a new tier, you’ll have a set number of games where you are invulnerable to dropping out</li>
<li>After you play these games, if you fall a full tier below your placement in terms of MMR, a demotion warning will appear in your profile (IE: A Gold V player playing at a Silver V skill level would see this warning)</li>
<li>Continuing to lose games at 0 LP will result in a demotion to division I of the next lower tier</li>
</ul></li>
<li>Challenger pool in solo and duo ranked queues have been increased to 200 players (from 50)</li>
</ul><p><a href="#patch-top">Back to top</a></p>
</div></div>

<hr class="divider"><p class="pn_darkred">
		Hey Summoners,<br><br>
		Riot Pwyff here with the first update of 2014! Since this is the beginning of a new year, we’re rolling over our patch numbering from 3.x to 4.x. Aside from getting that +1, our general design philosophy for the new season isn’t changing. We’re still closely watching the state of the game as it evolves with the preseason, and this update is just another step down that path.
	</p>
		<p class="pn_darkred">
		The general theme of patch 4.1 is more refinement and tuning. We’re in the process of de-fogging more of those outliers like Shyvana, Riven and Annie. That said, now that the dust has settled we can also take another look at modernizing certain items (the Spirit line of jungle items, for example) and systems (2v1 lane swaps).
	</p>
		<p class="pn_darkred">
		Quick note, any text in red will be myself guiding you through some of our changes and our thought process behind them. On with the patch notes!
	</p>
	<hr class="divider"><header class="header-primary pn_heading"><a class="anchor" name="patch-Vision-Code-Changes" id="patch-Vision-Code-Changes"></a>
		<h2>
			Vision Code Changes
		</h2>
	</header><p class="pn_darkred">
		While these changes won’t be immediately noticeable at first, you should see some positive differences over time with vision becoming more dynamic and responsive. These changes let us do more with vision while tackling larger issues in 2014. No promises for now as to what timelines we’ll be hitting with our updates, but hopefully you’ll be able to see the difference.
	</p>
	<ul><li>In-game vision should now be more responsive and dynamic as it interacts with abilities (like Lux’s Lucent Singularity)
		</li>
		<li>Ward sweeping abilities will now cause wards to darken the fog of war when they are disabled.
		</li>
		<li>Projectiles should register more quickly when a champion casts an ability from the fog of war
		</li>
	</ul><p>
		<a href="#">Back to top</a>
	</p>
	<hr class="divider"><header class="header-primary pn_heading"><a class="anchor" name="patch-Champions" id="patch-Champions"></a>
		<h2>
			Champions
		</h2>
	</header><p>
		<a class="anchor" name="patch-anivia" id="patch-anivia"></a><strong class="anivia">Anivia</strong>
	</p>
	<p>
		<strong class="pn_black">Summary:</strong> We’ve increased the speed of Anivia’s basic attack projectile and brought down the mana cost of Flash Frost and Crystallize. Additionally, Anivia’s ult will now have a warning effect when she is almost out of Glacial Storm’s range.
	</p>
	<p class="pn_darkred">
		We call these kinds of changes “modernizations,” where we bring a champion up-to-date with the current state of the game. Usually this means mana cost changes (typically reductions), but it also extends to playability improvements, like range indicators or gameplay improvements.
	</p>
	<p>
		<strong>General</strong>
	</p>
	<ul><li>Basic attack projectile speed increased to 1400 (from 1100)
		</li>
	</ul><p>
		<strong>Q - Flash Frost</strong>
	</p>
	<ul><li>Mana cost reduced to 80/90/100/110/120 (from 80/100/120/140/160)
		</li>
	</ul><p>
		<strong>W – Crystalize</strong>
	</p>
	<ul><li>Mana reduced to 70 at all ranks (from 70/90/110/130/150)
		</li>
	</ul><p>
		<strong>R - Glacial Storm</strong>
	</p>
	<ul><li>Now displays a warning particle over Anivia that only the Anivia player can see when she is close to the maximum range of her ultimate
		</li>
	</ul><hr class="divider"><p>
		<a class="anchor" name="patch-annie" id="patch-annie"></a><strong class="annie">Annie</strong>
	</p>
	<p>
		<strong class="pn_black">Summary:</strong> Pyromania’s stun duration is now shorter at earlier levels but Summon: Tibbers’ cooldown has been reduced at later levels to compensate.
	</p>
	<p class="pn_darkred">
		Annie’s a terrifying kid to play against in the early phases of the game – even before Tibbers joins the party– mostly due to her long duration stun at level 1. With these changes, we want to give Annie’s lane opponents some more room to breathe against her early threat, but we’ve given Tibbers more late game playtime to compensate. This should lessen a bit of Annie’s power without incinerating her overall strengths.
	</p>
	<p>
		<strong>Passive - Pyromania</strong>
	</p>
	<ul><li>Stun duration now scales 1.25/1.5/1.75 seconds at levels 1/6/11 (from 1.75 seconds at all levels)
		</li>
	</ul><p>
		<strong>R - Summon Tibbers</strong>
	</p>
	<ul><li>Cooldown reduced to 120/100/80 seconds (from 120 at all ranks)</li>
	</ul><hr class="divider"><p>
		<a class="anchor" name="patch-evelynn" id="patch-evelynn"></a><strong class="evelynn">Evelynn</strong>
	</p>
	<p>
		<strong class="pn_black">Summary:</strong> We’ve slowed down Hate Spike’s missile speed, changed Ravage’s damage from magical to physical and Agony’s Embrace now grants its shield instantly.
	</p>
	<p class="pn_darkred">
		Evelynn’s newfound strength in the preseason is partially a result of our changes to the vision system, but she’s also been blowing up enemy champions during the mid-game when she can stack Magic Penetration. Changing Ravage’s damage to physical will reduce some of Evelynn’s burst, but our core focus is on the Hate Spike change. By reducing its missile speed, Evelynn’s victims should feel more rewarded for keeping her at a distance, especially if they have some speed boosts to outrange her spikes. We also cleaned up the response time of Agony’s Embrace – it feels pretty bad for Evelynn to die before the shield but <strong>after</strong> the cast.
	</p>
	<p>
		<strong>Q - Hate Spike</strong>
	</p>
	<ul><li>Missile speed reduced to 1250 (from 2000)
		</li>
	</ul><p>
		<strong>E - Ravage</strong>
	</p>
	<ul><li>Damage changed to physical (from magic)
		</li>
	</ul><p>
		<strong>R - Agony's Embrace</strong>
	</p>
	<ul><li>Now grants the shield instantly (instead of 0.5 seconds later after the projectiles return to her)
		</li>
	</ul><hr class="divider"><p>
		<a class="anchor" name="patch-janna" id="patch-janna"></a><strong class="janna">Janna</strong>
	</p>
	<p>
		<strong class="pn_black">Summary:</strong> Monsoon now channels immediately on activation and the channel duration has been reduced. Additionally, we’re increasing Monsoon’s heal per second and total heal amount.
	</p>
	<p class="pn_darkred">
		Since we removed a lot of Janna’s invisible power, we can now buff up some of the cooler parts of her kit. Since Monsoon is one of Janna’s big moments in the spotlight, we decided to throw caution to the wind and buff her up with more healing potential and immediate gains.
	</p>
	<p>
		<strong>R - Monsoon</strong>
	</p>
	<ul><li>Now begins channeling immediately upon activation (from an initial 0.25 second cast time)
		</li>
		<li>Total channel duration reduced to 3 seconds (from 4 seconds)
		</li>
		<li>Heal per second increased to 100/150/200 (+0.6 ability power) (from 70/110/150 (+0.35 ability power)
		</li>
		<li>Total heal amount increased to 300/450/600 (+1.8 ability power) (from 280/440/600 (+1.4 ability power))
		</li>
	</ul><hr class="divider"><p>
		<a class="anchor" name="patch-jinx" id="patch-jinx"></a><strong class="jinx">Jinx</strong>
	</p>
	<p>
		<strong class="pn_black">Summary:</strong> We’ve lowered Jinx’s base health but increased her health per level. We’re also standardizing Flame Chompers! arm time, reduced its damage at earlier levels and fixing a slight bug.
	</p>
	<p class="pn_darkred">
		Given all of Jinx’s other core strengths; we’re looking to reduce some of her early game laning power.<br><br>
		Despite Flame Chompers! taking a little longer to arm – and doing less damage at early levels –  Jinx can at least get excited about their increased reliability against champions who could use movement-based abilities to get over them (Vi, Lee Sin, etc).
	</p>
	<p>
		<strong>General</strong>
	</p>
	<ul><li>Base health reduced to 462 (from 500)
		</li>
		<li>Health per level increased to 82 (from 80)
		</li>
	</ul><p>
		<strong>E - Flame Chompers!</strong>
	</p>
	<ul><li>Standardized arm time to 0.7 seconds (from 0.5 to 0.74 seconds)
		</li>
		<li>Fixed a bug that would cause Chompers to sometimes not activate on champions dashing over them
		</li>
		<li>Reduced damage at earlier levels to 80/135/190/245/300 (from 100/150/200/250/300)
		</li>
	</ul><hr class="divider"><p>
		<a class="anchor" name="patch-nasus" id="patch-nasus"></a><strong class="nasus">Nasus</strong>
	</p>
	<p>
		<strong class="pn_black">Summary:</strong> We’re reducing Wither’s range and removing the increased Wither and Spirit Fire range granted through Fury of the Sands.
	</p>
	<p class="pn_darkred">
		We want to keep Nasus’ core strengths where they are while toning down some of his power. Reducing the cast range of Wither and removing Wither’s bonus cast range from Fury of the Sands means that Nasus must position better and make better decisions about which target to Wither, rather than “sniping” fragile opponents with long-range Withers before eating them.
	</p>
	<p>
		<strong>W - Wither</strong>
	</p>
	<ul><li>Range reduced to 600 (from 700)
		</li>
	</ul><p>
		<strong>R - Fury of the Sands</strong>
	</p>
	<ul><li>No longer increases the cast ranges of Wither and Spirit Fire</li>
	</ul><hr class="divider"><p>
		<a class="anchor" name="patch-rengar" id="patch-rengar"></a><strong class="rengar">Rengar</strong>
	</p>
	<p>
		<strong class="pn_black">Summary:</strong> We fixed a bunch of bugs in Savagery and tweaked Empowered Battle Roar.
	</p>
	<p class="pn_darkred">
		Rengar’s overall sustain with Empowered Battle Roar is incredibly high, especially because he can transition from a tank in the mid-game to an unseen predator in the late-game. While this is definitely a nerf to Rengar’s overall sustain, we wanted to maintain his ability to survive in a fight, so the scaling heal will remain a good choice when Rengar’s low on health. The Savagery change means that Rengar now has two seconds to pull off his empowered basic attack instead of six seconds.
	</p>
	<p>
		<strong>Q – Savagery</strong>
	</p>
	<ul><li>Savagery basic attack buff (not attack speed buff) reduced to 2 seconds (from 6 seconds)</li>
		<li>Fixed multiple bugs that allowed Rengar to use Savagery more frequently than intended</li>
	</ul><p>
		<strong>W - Battle Roar</strong>
	</p>
	<ul><li>Empowered Battle Roar heal changed to 20 + (10 x level) amplified by a 1% per 1% of Rengar’s missing health (from 40 + (20 x level) health)</li>
	</ul><hr class="divider"><p>
		<a class="anchor" name="patch-riven" id="patch-riven"></a><strong class="riven">Riven</strong>
	</p>
	<p>
		<strong class="pn_black">Summary:</strong> We lowered Riven’s base attack damage but increased her attack damage per level to compensate. Additionally, Broken Wings’ damage has been decreased and Valor’s shield duration was shortened.
	</p>
	<p class="pn_darkred">
		Riven’s burst damage is just too high at early levels, so we’re dulling her blade. Broken Wings scaling off total AD from bonus AD means that Riven will do better when she’s behind (because she won’t be able to buy so much raw AD), but worse when she’s far ahead. We’re reducing the duration of Valor to hit some of Riven’s flexibility in lane – if she’s going to rely on Valor’s shield to trade damage, she’ll need to commit quickly or risk losing the shield.
	</p>
	<p>
		<strong>General</strong>
	</p>
	<ul><li>Base attack damage reduced to 54 (from 56.7)
		</li>
		<li>Attack damage per level increased to 3 (from 2.7)
		</li>
	</ul><p>
		<strong>Q - Broken Wings</strong>
	</p>
	<ul><li>Damage per strike decreased to 10/30/50/70/90 (+40/45/50/55/60% total attack damage) (from 30/55/80/105/130 (+70% bonus attack damage))
		</li>
	</ul><p>
		<strong>E - Valor</strong>
	</p>
	<ul><li>Shield duration reduced to 1.5 seconds (from 2.5 seconds)
		</li>
	</ul><hr class="divider"><p>
		<a class="anchor" name="patch-shyvana" id="patch-shyvana"></a><strong class="shyvana">Shyvana</strong>
	</p>
	<p>
		<strong class="pn_black">Summary:</strong> We’re lowering Burnout’s overall damage and Flame Breath’s damage at earlier levels. Dragon’s Descent also received a bug fix and the damage now matches the tooltip.
	</p>
	<p class="pn_darkred">
		Shyvana’s doing a lot of damage in the early to mid-game, even when she builds tanky. It turns out that when Shyvana can push, wave clear and out-trade opponents all at once, things can get a little frustrating. We’re cooling some of that dragon blood down to make her early bullying more manageable.
	</p>
	<p>
		<strong>W - Burnout</strong>
	</p>
	<ul><li>Damage per second reduced to 20/35/50/65/80 (from 25/40/55/70/85)
		</li>
		<li>Minimum magic damage reduced to 60/105/150/195/240 (from 75/120/165/210/255)
		</li>
		<li>Maximum magic damage reduced to 140/245/350/455/560 (from 175/280/385/490/595)
		</li>
	</ul><p>
		<strong>E - Flame Breath</strong>
	</p>
	<ul><li>Damage reduced at earlier levels to 60/100/140/180/220 (from 80/115/150/185/220)
		</li>
	</ul><p>
		<strong>R - Dragon's Descent</strong>
	</p>
	<ul><li>Damage now correctly matches the tooltip at 175/300/425 (from 200/300/400)
		</li>
	</ul><hr class="divider"><p>
		<a class="anchor" name="patch-thresh" id="patch-thresh"></a><strong class="thresh">Thresh</strong>
	</p>
	<p>
		<strong class="pn_black">Summary:</strong> We made it more obvious that Dark Passage is a clickable item and gave it a small collision radius. We’re also slightly increasing the time it takes for The Box to form.
	</p>
	<p class="pn_darkred">
		Giving Thresh’s lantern a small collision radius means that he can’t cover things like minions or wards to prevent enemies from clicking them. Similarly, this also means that minions can’t accidentally cover up a lantern, and that enemies can’t block Thresh’s lantern with a bunch of wards. Instead, Thresh’s lantern will now slightly push units out of the way when it’s thrown on top of them – or if the unit can’t be moved (ie: wards or champions), Thresh’s lantern will land beside them and champions won’t be able to walk directly on top of it. Click that lantern!<br><br>
		The increased delay on The Box’s wall formation is to clarify its use as a trapping ability when Thresh has the right position. Sometimes Thresh will just throw out The Box when walking behind a fleeing opponent to “clip” them on the edge, but Thresh should fail in those situations because he didn’t bother to have the proper positioning.
	</p>
	<p>
		<strong>W - Dark Passage</strong>
	</p>
	<ul><li>Now shows indicator particles to nearby allies to indicate that it is a clickable object
		</li>
		<li>Lantern now has a small collision radius
		</li>
	</ul><p>
		<strong>R - The Box</strong>
	</p>
	<ul><li>Delay on wall formation increased to 0.75 seconds (from 0.5 seconds)
		</li>
	</ul><hr class="divider"><p>
		<a class="anchor" name="patch-yasuo" id="patch-yasuo"></a><strong class="yasuo">Yasuo</strong>
	</p>
	<p>
		<strong class="pn_black">Summary:</strong> We’ve smoothed out Yasuo’s gameplay overall and made Steel Tempest auto-smartcast. Additionally, Last Breath now gives maximum Flow when cast and has a slightly shortened animation.
	</p>
	<p class="pn_darkred">
		While we’re still keeping an eye on how Yasuo is performing at all levels, we wanted to smooth out his gameplay and give Last Breath more use to turn around a fight when he’s behind.
	</p>
	<p>
		<strong>General</strong>
	</p>
	<ul><li>Now properly applies the 10% critical strike damage reduction to Statikk Shiv critical hits
		</li>
