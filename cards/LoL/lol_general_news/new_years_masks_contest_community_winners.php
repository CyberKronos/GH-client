<hr class="divider"><p></p><h4><strong>Update: Judging and future contests</strong></h4>

<p>Due to some confusion with the final 100 entries available for voting we wanted to clarify how we narrowed down the group. After reviewing close to two thousand submissions, we judged the entries on overall quality and adherence to the provided guidelines. Based on those metrics we felt confident we’d provided the best subsection for your consideration and voting. It’s quite obvious that not everyone agrees with that opinion. We’ve heard your feedback and will be revisiting our voting system for future events in North America. Going forward, with the exception of screening out inappropriate submissions, we’ll be putting the voting process entirely into the hands of the community in the interest of ensuring the absolute best creations make their way to victory. We’ll have more info on the new system with the announcement of our next contest (pro tip: keep an eye out around Valentine’s Day) but rest assured it will include safeguards for social engineering, stolen work and all other sorts of voting shenanigans.</p> 

